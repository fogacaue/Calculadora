// Esta é uma classe chamada Subtração.
public class Subtracao {
    // Este é um método chamado Subtracao que retorna um inteiro.
    // Ele recebe dois parâmetros, e e f, ambos são inteiros.
    public int subtracao(int e, int f) {
        // Este método simplesmente retorna a subtração dos dois parâmetros e e f.
        return e - f;
    }

}