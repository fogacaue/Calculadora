// Esta é uma classe chamada Soma.
public class Soma {
    // Este é um método chamado soma que retorna um inteiro.
    // Ele recebe dois parâmetros, a e b, ambos são inteiros.
    public int soma(int a, int b) {
        // Este método simplesmente retorna a soma dos dois parâmetros a e b.
        return a + b;
    }
}