// Esta é uma classe chamada Multílicação.
public class Multiplicacao {
    // Este é um método chamado Multiplicacao que retorna um inteiro.
    // Ele recebe dois parâmetros, c e d, ambos são inteiros.
    public int multiplicacao(int c, int d) {
        // Este método simplesmente retorna a multiplicação dos dois parâmetros c e d.
        return c * d;
    }
}